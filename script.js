function updateLanguage(lang) {
    const t = translations[lang] || translations.en;
    
    const titleEl = document.querySelector('.top-bar h1');
    if (titleEl) titleEl.textContent = t.title;
    
    const placeholderEl = document.getElementById('queryInput');
    if (placeholderEl) placeholderEl.placeholder = t.placeholder;
    
    const askBtnEl = document.getElementById('askBtn');
    if (askBtnEl && !askBtnEl.disabled) askBtnEl.textContent = t.askBtn;
    
    document.querySelectorAll('.nav-item').forEach((item, idx) => {
        const keys = ['profile', 'about', 'howto'];
        if (t[keys[idx]]) item.textContent = t[keys[idx]];
    });
    
    document.querySelectorAll('.stat-label').forEach((label, idx) => {
        const keys = ['queries', 'modulesUsed', 'user'];
        if (t[keys[idx]]) label.textContent = t[keys[idx]];
    });
    
    if (t.modules) {
        document.querySelectorAll('.module-card').forEach(card => {
            const module = card.getAttribute('data-module');
            if (t.modules[module]) {
                const h3 = card.querySelector('h3');
                const p = card.querySelector('p');
                if (h3) h3.textContent = t.modules[module].title;
                if (p) p.textContent = t.modules[module].desc;
            }
        });
    }
}

