if (document.getElementById('micBtn')) {
    const micBtn = document.getElementById('micBtn');
    const queryInput = document.getElementById('queryInput');
    const askBtn = document.getElementById('askBtn');
    const responseArea = document.getElementById('responseArea');
    const listeningStatus = document.getElementById('listeningStatus');
    
    document.querySelectorAll('.module-card').forEach(card => {
        card.addEventListener('click', function() {
            document.querySelectorAll('.module-card').forEach(c => c.classList.remove('active'));
            this.classList.add('active');
            selectedModule = this.getAttribute('data-module');
        });
    });
    
    let recognition = null;
    if ('webkitSpeechRecognition' in window || 'SpeechRecognition' in window) {
        const SpeechRecognition = window.SpeechRecognition || window.webkitSpeechRecognition;
        recognition = new SpeechRecognition();
        recognition.continuous = false;
        recognition.interimResults = false;
        recognition.lang = userData.language === 'hi' ? 'hi-IN' : 'en-IN';
        
        recognition.onstart = function() {
            micBtn.classList.add('listening');
            listeningStatus.textContent = 'Listening...';
        };
        
        recognition.onresult = function(event) {
            const transcript = event.results[0][0].transcript;
            queryInput.value = transcript;
            listeningStatus.textContent = 'Got it!';
        };
        
        recognition.onerror = function(event) {
            listeningStatus.textContent = 'Error: ' + event.error;
            micBtn.classList.remove('listening');
        };
        
        recognition.onend = function() {
            micBtn.classList.remove('listening');
            setTimeout(() => {
                listeningStatus.textContent = '';
            }, 2000);
        };
    }
    
    micBtn.addEventListener('click', function() {
        if (recognition) {
            recognition.start();
        } else {
            alert('Speech recognition not supported');
        }
    });
    
    askBtn.addEventListener('click', async function() {
        const query = queryInput.value.trim();
        if (!query) {
            alert('Please enter a question');
            return;
        }
        
        askBtn.disabled = true;
        askBtn.textContent = 'Processing...';
        
        try {
            const response = await queryBackend(query, selectedModule);
            displayResponse(response);
            
            userData.totalQueries++;
            localStorage.setItem('totalQueries', userData.totalQueries);
            if (document.getElementById('totalQueries')) {
                document.getElementById('totalQueries').textContent = userData.totalQueries;
            }
            
            if (selectedModule) {
                const usedModules = new Set(JSON.parse(localStorage.getItem('usedModules') || '[]'));
                usedModules.add(selectedModule);
                localStorage.setItem('usedModules', JSON.stringify([...usedModules]));
                if (document.getElementById('modulesUsed')) {
                    document.getElementById('modulesUsed').textContent = usedModules.size;
                }
            }
        } catch (error) {
            displayError(error.message);
        } finally {
            askBtn.disabled = false;
            const t = translations[userData.language] || translations.en;
            askBtn.textContent = t.askBtn;
        }
    });
    
    queryInput.addEventListener('keypress', function(e) {
        if (e.key === 'Enter' && !e.shiftKey) {
            e.preventDefault();
            askBtn.click();
        }
    });
}

async function queryBackend(query, module) {
    const response = await fetch('/query', {
        method: 'POST',
        headers: {
            'Content-Type': 'application/json'
        },
        body: JSON.stringify({
            query: query,
            module: module || 'general',
            language: userData.language
        })
    });
    
    if (!response.ok) {
        throw new Error('Failed to get response from server');
    }
    
    return await response.json();
}

function displayResponse(data) {
    const responseArea = document.getElementById('responseArea');
    const answer = data.answer || data.response || 'No response received';
    
    responseArea.style.display = 'block';
    responseArea.innerHTML = `
        <div class="response-content">
            <h3>Response:</h3>
            <p>${answer}</p>
            <button class="play-voice-btn" onclick="speakResponse('${answer.replace(/'/g, "\\'")}')">
                <svg width="16" height="16" viewBox="0 0 16 16" fill="none">
                    <path d="M3 4L13 8L3 12V4Z" fill="white"/>
                </svg>
                Play Voice
            </button>
        </div>
    `;
}

function displayError(message) {
    const responseArea = document.getElementById('responseArea');
    responseArea.style.display = 'block';
    responseArea.innerHTML = `
        <div class="response-content">
            <h3 style="color: #E55A2B;">Error:</h3>
            <p>${message}</p>
        </div>
    `;
}

function speakResponse(text) {
    if ('speechSynthesis' in window) {
        const utterance = new SpeechSynthesisUtterance(text);
        utterance.lang = userData.language === 'hi' ? 'hi-IN' : 'en-IN';
        utterance.rate = 0.9;
        window.speechSynthesis.speak(utterance);
    } else {
        alert('Text-to-speech not supported');
    }
}

